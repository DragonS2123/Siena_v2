"""Общие типы ошибок Siena v2. Различие важно для раздела 7 ARCHITECTURE.md:

- SienaToolError    — ожидаемая, восстановимая ошибка исполнения инструмента
                      (недоступен сайт, пусто в памяти, невалидные аргументы модели).
                      Возвращается модели как ToolResult(ok=False, ...) — модель сама
                      решает, что делать дальше.

- SienaInfraError   — сбой инфраструктуры самого Runtime (Ollama недоступен,
                      повреждён/заблокирован файл памяти). Спрашивать модель
                      не у кого — ошибка логируется и эскалируется пользователю
                      напрямую, минуя модель.
"""


class SienaToolError(Exception):
    pass


class SienaInfraError(Exception):
    pass
